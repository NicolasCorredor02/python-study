def is_anagram(word1, word2):
  if word1.lower() == word2.lower():
    return False
     #return list(word1.lower()).sort() == list(word2.lower()).sort()
    
  return sorted(word1.lower()) == sorted(word2.lower())

print(is_anagram("amor", "roma"))
  